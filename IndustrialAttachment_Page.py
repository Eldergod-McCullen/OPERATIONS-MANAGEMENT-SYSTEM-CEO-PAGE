    def _apply_filters(self):
        text   = self.search_edit.text().lower()
        status = self._status_combo.currentText()
        dept   = self._dept_combo.currentText()
        results = []
        for app in self._applicants:
            app_id, name, inst, course, date, app_status = app
            if status != "All Status"    and app_status != status: continue
            if text and not any(text in v.lower() for v in
                                (app_id, name, inst, course)): continue
            results.append(app)
        self._populate_table(results)

    def _on_reset(self):
        self.search_edit.clear()
        self._status_combo.setCurrentIndex(0)
        self._dept_combo.setCurrentIndex(0)
        self._populate_table()

    def add_applicant(self, data: dict):
        new_id = f"APP-{128 + len(self._applicants):05d}"
        from datetime import date
        self._applicants.insert(0, (
            new_id, data["name"], data["institution"],
            data["course"], date.today().strftime("%d %b %Y"), data["status"]
        ))
        self._populate_table()

    def _on_view(self, row):
        if row >= len(self._applicants): return
        a = self._applicants[row]
        QtWidgets.QMessageBox.information(
            self, "Applicant Details",
            f"<b>{a[1]}</b><br>ID: {a[0]}<br>Institution: {a[2]}<br>"
            f"Course: {a[3]}<br>Applied: {a[4]}<br>Status: {a[5]}"
        )

    def _on_edit(self, row):
        if row >= len(self._applicants): return
        a = self._applicants[row]
        dlg = NewApplicantDialog(self, data={
            "name": a[1], "institution": a[2],
            "course": a[3], "status": a[5], "email": ""
        })
        dlg.applicant_saved.connect(lambda d, r=row: self._save_edit(r, d))
        dlg.exec()

    def _save_edit(self, row, data):
        old = self._applicants[row]
        self._applicants[row] = (
            old[0], data["name"], data["institution"],
            data["course"], old[4], data["status"]
        )
        self._populate_table()

    def _on_delete(self, row):
        if row >= len(self._applicants): return
        name = self._applicants[row][1]
        reply = QtWidgets.QMessageBox.question(
            self, "Delete Applicant",
            f"Delete <b>{name}</b>? This cannot be undone.",
            QtWidgets.QMessageBox.StandardButton.Yes |
            QtWidgets.QMessageBox.StandardButton.No,
            QtWidgets.QMessageBox.StandardButton.No,
        )
        if reply == QtWidgets.QMessageBox.StandardButton.Yes:
            self._applicants.pop(row); self._populate_table()

    def _on_more(self, row, btn):
        menu = QtWidgets.QMenu(self)
        menu.setStyleSheet(f"""
            QMenu {{
                background:{_WHITE}; border:1px solid {_BORDER};
                border-radius:6px; padding:4px 0; font-size:12px;
                font-family:'Segoe UI',Arial;
            }}
            QMenu::item {{ color:{_TEXT_MID}; padding:8px 20px; }}
            QMenu::item:selected {{ background:#eff6ff; color:{_NAVY}; }}
            QMenu::separator {{ height:1px; background:{_BORDER}; margin:4px 0; }}
        """)
        menu.addAction("📅  Schedule Interview")
        menu.addAction("✅  Approve Application")
        menu.addAction("❌  Reject Application")
        menu.addSeparator()
        menu.addAction("📄  View Full Profile")
        pos = btn.mapToGlobal(QtCore.QPoint(0, btn.height()))
        menu.exec(pos)


