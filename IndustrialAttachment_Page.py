    def _on_save(self):
        name   = self.name_edit.text().strip()
        email  = self.email_edit.text().strip()
        inst   = self.inst_edit.text().strip()
        course = self.course_edit.text().strip()
        dept   = self.dept_combo.currentText()
        errs   = []
        if not name:   errs.append("Full Name required.")
        if not email or "@" not in email: errs.append("Valid email required.")
        if not inst:   errs.append("Institution required.")
        if not course: errs.append("Course required.")
        if dept.startswith("—"): errs.append("Select a Department.")
        if errs:
            self.err_lbl.setText("⚠  " + "  •  ".join(errs))
