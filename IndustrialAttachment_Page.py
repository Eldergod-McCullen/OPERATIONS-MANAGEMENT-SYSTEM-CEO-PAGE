    def _populate_table(self, data: list = None):
        rows = data if data is not None else self._applicants
        self.table.setRowCount(0)
        for i, app in enumerate(rows):
            app_id, name, inst, course, date, status = app
            self.table.insertRow(i)
            self.table.setRowHeight(i, 48)

            # Checkbox
            chk = QtWidgets.QCheckBox()
            chk_w = QtWidgets.QWidget()
            ch = QtWidgets.QHBoxLayout(chk_w)
            ch.addWidget(chk); ch.setAlignment(Qt.AlignmentFlag.AlignCenter)
            ch.setContentsMargins(0,0,0,0)
            self.table.setCellWidget(i, 0, chk_w)

            for col, txt in [(1,app_id),(2,name),(3,inst),(5,date)]:
                it = QtWidgets.QTableWidgetItem(txt)
                it.setFlags(it.flags() & ~Qt.ItemFlag.ItemIsEditable)
                if col == 2:
                    it.setFont(QtGui.QFont("Segoe UI",11,QtGui.QFont.Weight.Bold))
